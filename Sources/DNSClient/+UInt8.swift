extension UInt8 {
    @inlinable
    static var asciiLineFeed: UInt8 {
        0x0A
    }

    @inlinable
    static var asciiCarriageReturn: UInt8 {
        0x0D
    }

    @inlinable
    static var asciiWhitespace: UInt8 {
        0x20
    }

    @inlinable
    static var asciiPercent: UInt8 {
        0x25
    }

    /// aka "Number sign"
    @inlinable
    static var asciiHashtag: UInt8 {
        0x23
    }
}
